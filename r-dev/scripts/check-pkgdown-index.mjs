#!/usr/bin/env node
// Guardrail: keep the pkgdown reference index in sync with the package's
// exported topics. A new export (added to NAMESPACE by devtools::document())
// that is not listed in _pkgdown.yml makes `pkgdown::build_site()` fail in CI
// with "N topic(s) missing from index". This hook runs pkgdown::check_pkgdown()
// right after the two events that can make the index stale, and blocks (exit 2)
// so the mistake is fixed before moving on.
//
// Triggers (decided in tool_name / tool_input on stdin):
//   - devtools::document() ran         (Bash command containing "document(")
//   - _pkgdown.yml/.yaml was edited     (Edit/Write/MultiEdit of the config)
//
// NAMESPACE is intentionally NOT a trigger: it is never hand-edited (it is
// regenerated by document()), so document() is the real signal.
//
// No-op unless a _pkgdown.yml exists at/above the working directory, so this
// is silent in non-pkgdown packages and non-R projects.
//
// Wired up via a PostToolUse hook in this plugin's hooks/hooks.json.

import {
  readPayload,
  cwd as payloadCwd,
  onPath,
  run,
  findUp,
  block,
  existsSync,
  join,
  basename,
} from "./lib/hook.mjs";

const payload = readPayload();
const tool = payload?.tool_name || "";
const cmd = payload?.tool_input?.command || "";
const file = payload?.tool_input?.file_path || "";
const cwd = payloadCwd(payload);

// pkgdown accepts _pkgdown.yml or _pkgdown.yaml, at the root or under pkgdown/.
const isPkgdownConfig = (name) => name === "_pkgdown.yml" || name === "_pkgdown.yaml";
const hasPkgdownConfig = (dir) =>
  existsSync(join(dir, "_pkgdown.yml")) ||
  existsSync(join(dir, "_pkgdown.yaml")) ||
  existsSync(join(dir, "pkgdown", "_pkgdown.yml")) ||
  existsSync(join(dir, "pkgdown", "_pkgdown.yaml"));

// Decide whether this tool call is a trigger.
let trigger = false;
switch (tool) {
  case "Bash":
    // devtools::document() / roxygen2::roxygenise() run from a shell.
    if (/document\(|roxygenise\(|roxygenize\(/.test(cmd)) trigger = true;
    break;
  case "Edit":
  case "Write":
  case "MultiEdit":
    if (isPkgdownConfig(basename(file))) trigger = true;
    break;
}

if (!trigger) process.exit(0);

// Find the package root: the nearest directory at/above CWD holding a
// _pkgdown.yml. No _pkgdown.yml anywhere -> nothing to check, no-op.
const pkgRoot = findUp(cwd, hasPkgdownConfig);
if (!pkgRoot) process.exit(0);

// A pkgdown site needs a DESCRIPTION alongside the config; bail quietly if the
// _pkgdown.yml is not actually at an R package root.
if (!existsSync(join(pkgRoot, "DESCRIPTION"))) process.exit(0);

if (!onPath("Rscript")) process.exit(0);

// Run the check. check_pkgdown() errors (non-zero) when a topic is missing from
// or dangling in the index; it prints "No problems found." and exits 0 when the
// index is in sync. Capture output so we can hand the actual message back.
const { out, status } = run(
  "Rscript",
  ["-e", 'pkgdown::check_pkgdown(".")'],
  { cwd: pkgRoot, env: { NOT_CRAN: "true" } },
);

if (status !== 0) {
  block(
    [
      "pkgdown reference index is out of sync (pkgdown::check_pkgdown() failed):",
      "",
      out.trimEnd(),
      "",
      "Fix _pkgdown.yml: add the missing topic(s) to the reference index",
      "(or mark the function @keywords internal to drop it from the index),",
      "then re-run devtools::document().",
    ].join("\n"),
  );
}

process.exit(0);
